public class cast {
    public static void main(String args[])
    {

        byte b=127;
        int a=257;
        b=(byte)a;
        System.out.println(b);

        float ft=5.6f;
        int t= (int) ft;
        System.out.println(t);


        byte h=10;
        byte o= 30;
        int mul=h * o;
        System.out.println(mul);
        
    }
}
    
