public class decimal {
    public static void main(String[] args) {
        //literals
        int num1= 0x7E; //hexadecimal
        System.out.println(num1);

        int num2 = 1000_0000_0;
        System.out.println(num2);

        double num3 = 12e10;
        System.out.println(num3);

        char c='a';
        c++;
        System.out.println(c);
    }
    
}
